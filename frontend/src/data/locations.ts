export function getStatesForAutocomplete(): Array<{ value: string; label: string }> {
  return US_STATES.map(s => ({ value: s.name, label: s.name }));
}

export function getCitiesForState(stateName: string): Array<{ value: string; label: string }> {
  const state = US_STATES.find(s => s.name.toLowerCase() === stateName.toLowerCase());
  if (!state) return [];
  const cities = MAJOR_CITIES[state.code] || [];
  return cities.map(c => ({ value: c, label: c }));
}
